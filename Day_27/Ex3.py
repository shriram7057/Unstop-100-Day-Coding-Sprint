def max_cards(n, positions):
    positions.sort()

    # Separate positions into left and right relative to 0
    left_positions = [cards for pos, cards in positions if pos < 0]
    right_positions = [cards for pos, cards in positions if pos > 0]

    left_positions.reverse()

    total_cards = 0
    index_left, index_right = 0, 0
    turn = 'right'

    while index_left < len(left_positions) or index_right < len(right_positions):
        if turn == 'right' and index_right < len(right_positions):
            total_cards += right_positions[index_right]
            index_right += 1
            turn = 'left'
        elif turn == 'left' and index_left < len(left_positions):
            total_cards += left_positions[index_left]
            index_left += 1
            turn = 'right'
        else:
            break

    return total_cards

